/**
 * computes correlation between an event and beeing a squirrel
 * returns a number indicating the correlation
 * @param {array} table sums of measurement
 */
function phi([n00, n01, n10, n11]) {
    return (n11 * n00 - n10 * n01) /
        Math.sqrt((n11 + n10) *
                    (n00 + n01) *
                    (n11 + n01) *
                    (n00 + n10));
}

/**
 * computes correlation between an event and beeing a squirrel
 * returns a number indicating the correlation
 * @param {array} table - sums of measurement
 */
function phi2d([[n00, n01], [n10, n11]]) {
    return (n11 * n00 - n10 * n01) /
        Math.sqrt((n11 + n10) *
                    (n00 + n01) *
                    (n11 + n01) *
                    (n00 + n10));
}

/**
 * returns an array with measurements for an event
 * @param {string} event name of a specific event 
 * @param {array} journal array of journal entries 
 */
function tableFor(event, journal) {
    let table = [0, 0, 0, 0];
    for (let i = 0; i < journal.length; i++) {
        let entry = journal[i], index = 0;
        if (entry.events.includes(event)) ++index;
        if (entry.squirrel) index  += 2;
        table[index] += 1;
    }
    return table;
}

/**
 * returns an array of unique events
 * @param {array} journal array of journal entries
 */
function allEvents(journal) {
    let allEvents = [];
    for(let entry of journal) {
        allEvents = allEvents.concat(entry.events.filter(event => {
            return !allEvents.includes(event);
        }));
    }
    return allEvents;
}

/**
 * returns an array of unique events (alternative)
 * @param {array} journal array of journal entries
 */
function allEvents(journal) {
    let allEvents = [];
    for(entry of journal) {
        for(event of entry.events) {
            if (allEvents.indexOf(event) == -1) allEvents.push(event);
        }
    }
    return allEvents;
}

/**
 * outputs the table of results to console
 * @param {array} table array of correlation results
 */
function printTable(table) {
    table.forEach(entry => {
        console.log(`${entry.event}: ${entry.value}`);
    });
}


